#include "libft.h"

static int	min(int a, int b)
{
	if (a < b && a > -1)
		return (a);
	return (b);
}

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	j;
	size_t	len;

	if (dstsize == 0)
		return (ft_strlen(src));
	len = ft_strlen(src) + min(dstsize, ft_strlen(dst));
	i = 0;
	j = 0;
	while (dst[i])
		i++;
	while (src[j] && i < dstsize - 1)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = 0;
	return (len);
}
